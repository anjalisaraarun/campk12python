import turtle
lib=turtle.Turtle()

lib.color('red','cyan')


lib.begin_fill()


lib.forward(100)
lib.left(90)
lib.forward(100)
lib.left(90)
lib.forward(100)
lib.left(90)
lib.forward(100)

lib.end_fill()

lib.penup()
lib.forward(150)
lib.pendown()

lib.begin_fill()

lib.forward(100)
lib.left(90)
lib.forward(100)
lib.left(90)
lib.forward(100)
lib.left(90)
lib.forward(100)

lib.end_fill()








turtle.done()